package tp1;

public class Porta {

	protected String nome;
	protected int entrada;
	protected int saida;
	protected int trancada;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getEntrada() {
		return entrada;
	}

	public void setEntrada(int entrada) {
		this.entrada = entrada;
	}

	public int getSaida() {
		return saida;
	}

	public void setSaida(int saida) {
		this.saida = saida;
	}

	public int getTrancada() {
		return trancada;
	}

	public void setTrancada(int trancada) {
		this.trancada = trancada;
	}

}